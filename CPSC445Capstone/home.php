<h2>Advanced Scuba Diver</h2>
 
<p>The SDI Advanced Scuba Diver Development Program was created to make truly “Advanced Divers.” During the course your Instructor will take you through four complete SDI, TDI, or ERDI specialty courses (for example; Deep, Navigation, Photo, Video, Intro to Tech, Tender, etc.) covering all the skills and subjects, so that by the end you will feel comfortable to conduct dives of a similar nature.Once you have logged your 25th dive (these can include any and all training dives), visit your local SDI facility with your logbook and verification of completing four SDI, TDI, and ERDI specialties and you will be awarded the SDI Advanced Scuba Diver certification.
