<h2>Rescue Diver</h2>
<p> The SDI Rescue Diver Course is designed to develop the knowledge and necessary skills to effectively perform diver rescues and assist and administer the necessary first aid in the event of an emergency. You will learn skills such as how to perform self rescues, buddy rescues, recognize and calm potential panic divers, administer proper first aid to divers that have experienced dive related injuries, conduct effective search patterns and manage accident scenes. After the SDI Rescue Diver Course you can enroll in the SDI Master Scuba Diver Development Program or start in the professional ranks and sign up for the SDI Divemaster Course. <p>
